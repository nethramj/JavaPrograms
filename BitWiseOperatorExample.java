package mathematicaloperations;

public class BitWiseOperatorExample {

	public static void main(String[] args) {
		
int a=4;
int b=6;
System.out.println("A & B= "+(a & b));
System.out.println("A | B= "+(a | b));
System.out.println("~ A ="+(~a));
System.out.println("A >> B= "+(a>>b));
System.out.println("A << B= "+(a<<b));
	}

}
